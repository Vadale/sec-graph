"""Scoring + report generator (PROTOCOL §4-5). Joins results/verdicts.json with the hand-labelled
truth.json and emits the BENCHMARK tables. Cheap + re-runnable after any truth amendment.

Primary endpoint: paired real/FP accuracy, exact McNemar (Arm B vs Arm A). Everything else: point
estimates + bootstrap 95% CIs. Prioritization: O3 vs O2. Plus the tool self-audit and decode variance.

Usage: python benchmark/report.py > benchmark/BENCHMARK_generated.md
"""
from __future__ import annotations

import json
import statistics as st
from pathlib import Path

import score

ROOT = Path(__file__).resolve().parent
DEV = {"pygoat"}


def _load(p):
    return json.loads(Path(p).read_text(encoding="utf-8"))


def truth_by_key(repo):
    t = _load(ROOT / "corpus" / repo / "truth.json")
    return {(f["key"]["rule_id"], f["key"]["file"], f["key"]["line"]): f for f in t["findings"]}


def v0(rec, arm):
    """First-run verdict dict for an arm."""
    a = rec["arms"].get(arm) or [{}]
    return (a[0] or {}).get("verdict") or {}


def meta0(rec, arm):
    a = rec["arms"].get(arm) or [{}]
    return (a[0] or {}).get("meta") or {}


def arm_metrics(findings, truth, arm):
    rf, gd, cl, sv, ptok, wall = [], [], [], [], [], []
    for rec in findings:
        key = tuple(rec["key"])
        tf = truth.get(key)
        if tf is None:
            continue
        v = v0(rec, arm)
        rf.append(score.realfp_correct(v, tf["real"]))
        g = score.guard_correct(v, tf["guard"])
        if g is not None:
            gd.append(g)
        if tf["real"]:
            cl.append(score.class_correct(v, tf["vuln_class"]))
        d = score.sev_dist(v, tf["severity"])
        if d is not None:
            sv.append(d)
        m = meta0(rec, arm)
        if m.get("prompt_tokens"):
            ptok.append(m["prompt_tokens"])
        if m.get("wall_s"):
            wall.append(m["wall_s"])
    return {"realfp": rf, "guard": gd, "class": cl, "sevdist": sv, "ptok": ptok, "wall": wall}


def pct(xs):
    return f"{100*score.acc(xs):.0f}% ({sum(1 for x in xs if x)}/{len([x for x in xs if x is not None])})"


def main():
    verd = _load(ROOT / "results" / "verdicts.json")
    print("<!-- generated by benchmark/report.py -->\n")
    print(f"Model `{verd['model']}` · num_ctx {verd['num_ctx']} · runs {verd['runs']} · seed {verd['seed']}\n")

    # ---- per-arm accuracy, per repo + pooled -------------------------------------------------
    pooled = {a: {"realfp": [], "guard": [], "class": [], "sevdist": [], "ptok": [], "wall": []} for a in "ABC"}
    mcnemar_pairs = []
    print("## Triage accuracy (first run)\n")
    print("| repo | n | arm | real/FP | guard | class (real only) | sev ±1 | med prompt tok | med s |")
    print("|---|---|---|---|---|---|---|---|---|")
    for repo, rep in verd["repos"].items():
        truth = truth_by_key(repo)
        F = rep["findings"]
        for arm in "ABC":
            m = arm_metrics(F, truth, arm)
            for k in pooled[arm]:
                pooled[arm][k] += m[k]
            sev1 = [d <= 1 for d in m["sevdist"]]
            tag = f"{repo}{' (dev)' if repo in DEV else ''}"
            print(f"| {tag if arm=='A' else ''} | {len(F) if arm=='A' else ''} | {arm} | {pct(m['realfp'])} | "
                  f"{pct(m['guard'])} | {pct(m['class'])} | {pct(sev1)} | "
                  f"{int(st.median(m['ptok'])) if m['ptok'] else '-'} | {round(st.median(m['wall']),1) if m['wall'] else '-'} |")
        # McNemar pairs (A vs B) for this repo
        for rec in F:
            tf = truth.get(tuple(rec["key"]))
            if tf is None:
                continue
            mcnemar_pairs.append((score.realfp_correct(v0(rec, "A"), tf["real"]),
                                  score.realfp_correct(v0(rec, "B"), tf["real"])))
    print("\n## Pooled (all repos)\n")
    print("| arm | real/FP | guard | class | sev ±1 | total prompt tok | total s |")
    print("|---|---|---|---|---|---|---|")
    for arm in "ABC":
        p = pooled[arm]
        print(f"| {arm} | {pct(p['realfp'])} | {pct(p['guard'])} | {pct(p['class'])} | "
              f"{pct([d<=1 for d in p['sevdist']])} | {sum(p['ptok'])} | {round(sum(p['wall']))} |")

    # ---- primary endpoint ---------------------------------------------------------------------
    mc = score.mcnemar_exact(mcnemar_pairs)
    a_acc = score.acc(pooled["A"]["realfp"])
    b_acc = score.acc(pooled["B"]["realfp"])
    idx = list(range(len(mcnemar_pairs)))
    ci = score.bootstrap_ci(idx, lambda s: score.acc([mcnemar_pairs[i][1] for i in s]) -
                            score.acc([mcnemar_pairs[i][0] for i in s]))
    print("\n## PRIMARY ENDPOINT — paired real/FP accuracy, Arm B vs Arm A\n")
    print(f"- Arm A {100*a_acc:.0f}%  ·  Arm B {100*b_acc:.0f}%  ·  Δ(B−A) = {100*(b_acc-a_acc):+.0f} pts, "
          f"bootstrap 95% CI [{100*ci[1]:+.0f}, {100*ci[2]:+.0f}] pts")
    print(f"- Exact McNemar: B-only-correct={mc['c_B_only']}, A-only-correct={mc['b_A_only']}, "
          f"discordant={mc['discordant']}, p={mc['p_value']}")
    print("- (pooled test assumes finding-level independence — see per-repo tables)")

    # ---- prioritization O3 vs O2 --------------------------------------------------------------
    print("\n## Prioritization — O3 (sec-graph) vs O2 (severity-sorted SARIF)\n")
    print("| repo | rank-of-first-critical O2 | O3 | prec@5 O2/O3 | prec@10 O2/O3 |")
    print("|---|---|---|---|---|")
    for repo, rep in verd["repos"].items():
        truth = truth_by_key(repo)
        Tcrit = {k for k, f in truth.items() if f["real"] and f["severity"] == "critical"}
        Thigh = {k for k, f in truth.items() if f["real"] and f["severity"] in ("high", "critical")}
        def rank_first(order):
            for i, k in enumerate(order, 1):
                if tuple(k) in Tcrit:
                    return i
            return None
        def patk(order, k):
            top = [tuple(x) for x in order[:k]]
            return f"{sum(1 for x in top if x in Thigh)}/{min(k,len(top))}"
        o2 = rep["o2"]; o3 = rep["o3"]
        print(f"| {repo} | {rank_first(o2)} | {rank_first(o3)} | {patk(o2,5)} / {patk(o3,5)} | "
              f"{patk(o2,10)} / {patk(o3,10)} |")

    # ---- tool self-audit ----------------------------------------------------------------------
    print("\n## Tool self-audit — sec-graph's OWN guard verdict vs truth (analyzed findings)\n")
    tp=fp=tn=fn=unk=0
    for repo, rep in verd["repos"].items():
        truth = truth_by_key(repo)
        for a in rep["self_audit"]:
            tf = truth.get(tuple(a["key"]))
            if tf is None or tf["guard"] == "n/a":
                continue
            if a["guard_status"] != "analyzed":
                unk += 1; continue
            pred = "unguarded" if a["unguarded"] else "guarded"
            if pred == "unguarded" and tf["guard"] == "unguarded": tp += 1
            elif pred == "unguarded" and tf["guard"] == "guarded": fp += 1
            elif pred == "guarded" and tf["guard"] == "guarded": tn += 1
            else: fn += 1
    print(f"- 'unguarded' precision: {tp}/{tp+fp} ; 'guarded' precision: {tn}/{tn+fn} ; unknown/non-analyzed: {unk}")

    # ---- decode variance ----------------------------------------------------------------------
    if verd["runs"] >= 2:
        same=tot=0
        for repo, rep in verd["repos"].items():
            for rec in rep["findings"]:
                for arm in "ABC":
                    a = rec["arms"].get(arm) or []
                    if len(a) >= 2:
                        tot += 1
                        if (a[0].get("verdict") or {}).get("verdict") == (a[1].get("verdict") or {}).get("verdict"):
                            same += 1
        print(f"\n## Decode variance — run0 vs run1 verdict agreement: {same}/{tot} ({100*same/tot:.0f}%)" if tot else "")


if __name__ == "__main__":
    main()
